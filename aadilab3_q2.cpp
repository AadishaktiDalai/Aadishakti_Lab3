//Library
#include <iostream>
using namespace std;
int main(){
	//Defining variables
	int a,b;
	int sum;
	int subs;
	int mlt;
	int div;
	int mod;
	// Initialising variables
	a=11;
	b=2;
	sum= a + b;
	subs= a - b;
	mlt= a * b;
	div= a / b;
	mod= a % b;
	//Displaying values
	cout<<"sum = "<<sum<<endl;
	cout<<"difference = "<<subs<<endl;
	cout<<"product = "<<mlt<<endl;
	cout<<"quotient = "<<div<<endl;
	cout<< "remainder = "<<mod<<endl;
return 0;
}

	
